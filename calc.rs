pub fn add(first_number: i64, second_number: i64) -> i64 { 
    return first_number + second_number; 
}

pub fn subtract(first_number: i64, second_number: i64) -> i64 { 
    return first_number - second_number; 
}

pub fn multiply(first_number: i64, second_number: i64) -> i64 { 
    return first_number * second_number; 
}
pub fn divide(first_number: i64, second_number: i64) -> i64 { 
    return first_number / second_number; 
}
